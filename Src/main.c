void customIRQ_UART_RxIDLE(void)
{
	//https://github.com/MaJerle/STM32_USART_DMA_RX/blob/master/projects/idle_line_irq_F4/Src/main.c

	static uint16_t OldLength = 0;	
	uint16_t RxLength = 0;

	if(__HAL_UART_GET_IT_SOURCE(&huart2, UART_IT_IDLE)  && __HAL_UART_GET_FLAG(&huart2, UART_FLAG_IDLE))
	{
		__HAL_UART_CLEAR_IDLEFLAG(&huart2);

		RxLength = ARRAY_LEN(aRxDMABuffer) - (__HAL_DMA_GET_COUNTER(&hdma_usart2_rx));	//calculate how many data in DMA buffer
//		printf("RX length : 0x%2X \r\n" , RxLength);

		if (RxLength != OldLength) 
		{                      
			if (RxLength > OldLength) 
			{
				HAL_UART_Transmit_DMA(&huart2,&aRxDMABuffer[OldLength],RxLength-OldLength);
			} 
			else 
			{
				HAL_UART_Transmit_DMA(&huart2,&aRxDMABuffer[OldLength],ARRAY_LEN(aRxDMABuffer)-OldLength);
				if (RxLength) 
				{
					HAL_UART_Transmit_DMA(&huart2,&aRxDMABuffer[0],RxLength);
				}
			}
		}

		OldLength = (OldLength == ARRAY_LEN(aRxDMABuffer))?(0):(RxLength);
	}
}

